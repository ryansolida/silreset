function urlify(string){
	return string.replace(/ /g,'-')
		.replace(/\'/g,'')
		.replace(/\"/g,'')
		.replace(/\,/g,'')
		.replace(/\>/g,'')
		.replace(/\</g,'')
		.replace(/\//g,'')
		.replace(/\#/g,'')
		.replace(/\?/g,'')
		.replace(/\;/g,'')
		.replace(/\:/g,'')
		.replace(/\./g,'')
		.replace(/\\/g,'')
		.replace(/\{/g,'')
		.replace(/\}/g,'')
		.replace(/\[/g,'')
		.replace(/\]/g,'')
		.replace(/\$/g,'')
		.replace(/\=/g,'')
		.replace(/\+/g,'')				
		.replace(/\!/g,'')			
		.replace(/\@/g,'')			
		.replace(/\%/g,'')
		.replace(/\^/g,'')
		.replace(/\*/g,'')
		.replace(/\(/g,'')
		.replace(/\)/g,'')
		.replace(/\|/g,'')
		.replace(/\&/g,'and')
		.toLowerCase();
}